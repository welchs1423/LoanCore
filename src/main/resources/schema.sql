CREATE TABLE IF NOT EXISTS LOAN_APPLICATION (
    APPLICATION_ID VARCHAR(50) PRIMARY KEY,
    CUSTOMER_ID VARCHAR(50) NOT NULL,
    AMOUNT DECIMAL(20,2) NOT NULL,
    STATUS_CODE VARCHAR(20) NOT NULL,
    FILE_NAME VARCHAR(255),
    ADDRESS VARCHAR(500),
    DEL_YN CHAR(1) DEFAULT 'N',
    APPLIED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX IF NOT EXISTS IDX_LOAN_CUSTOMER_ID ON LOAN_APPLICATION(CUSTOMER_ID);

CREATE TABLE IF NOT EXISTS LOAN_MEMO (
    memo_id INT AUTO_INCREMENT PRIMARY KEY,
    application_id VARCHAR(50) NOT NULL,
    writer VARCHAR(50) NOT NULL,
    content VARCHAR(1000) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

DROP TABLE IF EXISTS audit_logs;
CREATE TABLE audit_logs (
    id INT AUTO_INCREMENT PRIMARY KEY,
    action_name VARCHAR(100) NOT NULL,
    target_id VARCHAR(50),
    action_detail VARCHAR(1000),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS audit_logs (
    id INT AUTO_INCREMENT PRIMARY KEY,
    action VARCHAR(100),
    target VARCHAR(100),
    actor VARCHAR(100),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS loan_applications (
    application_id VARCHAR(50) PRIMARY KEY,
    customer_id VARCHAR(50) NOT NULL,
    amount DECIMAL(18, 2) NOT NULL,
    status_code VARCHAR(20) DEFAULT 'PENDING',
    file_name VARCHAR(255),
    address VARCHAR(500),
    del_yn CHAR(1) DEFAULT 'N',
    applied_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS audit_logs (
    id INT AUTO_INCREMENT PRIMARY KEY,
    action_name VARCHAR(100),
    target_id VARCHAR(100),
    action_detail VARCHAR(1000),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE LOAN_SCHEDULE (
    ID BIGINT AUTO_INCREMENT PRIMARY KEY,
    APPLICATION_ID VARCHAR(50) NOT NULL,
    INSTALLMENT_MONTH INT NOT NULL,
    PRINCIPAL DECIMAL(19, 4) NOT NULL,
    INTEREST DECIMAL(19, 4) NOT NULL,
    DUE_DATE DATE NOT NULL,
    STATUS VARCHAR(20) DEFAULT 'WAITING'
);

CREATE TABLE COLLATERAL_INFO (
    ID BIGINT AUTO_INCREMENT PRIMARY KEY,
    APPLICATION_ID VARCHAR(50) NOT NULL,
    COLLATERAL_TYPE VARCHAR(50) NOT NULL,
    COLLATERAL_VALUE DECIMAL(19, 4) NOT NULL
);

CREATE TABLE QNA_BOARD (
    ID BIGINT AUTO_INCREMENT PRIMARY KEY,
    CUSTOMER_ID VARCHAR(50) NOT NULL,
    TITLE VARCHAR(200) NOT NULL,
    CONTENT TEXT NOT NULL,
    ANSWER_CONTENT TEXT,
    STATUS VARCHAR(20) DEFAULT 'WAITING',
    REG_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    ANS_DATE TIMESTAMP
);

CREATE TABLE PRE_INQUIRY_HISTORY (
    ID BIGINT AUTO_INCREMENT PRIMARY KEY,
    CUSTOMER_ID VARCHAR(50) NOT NULL,
    CREDIT_SCORE INT NOT NULL,
    ESTIMATED_RATE DECIMAL(5, 2) NOT NULL,
    ESTIMATED_LIMIT DECIMAL(19, 4) NOT NULL,
    INQUIRY_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE E_SIGNATURE (
    ID BIGINT AUTO_INCREMENT PRIMARY KEY,
    APPLICATION_ID VARCHAR(50) NOT NULL,
    CUSTOMER_ID VARCHAR(50) NOT NULL,
    SIGN_HASH VARCHAR(256) NOT NULL,
    SIGN_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);